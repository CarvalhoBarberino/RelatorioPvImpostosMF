-- Quadro com dados do cliente com impostos

SELECT 
	A1_NOME AS CLI_NOME, 
	A1_END AS CLI_ENDERECO, 
	A1_CEP AS CLI_CEP, 
	A1_MUN AS CLI_MUNICIPIO, 
	A1_ESTC AS CLI_UF, 
	A1_TEL AS CLI_TELEFONE, 
	A1_CGC AS CLI_CNPJ, 
	A1_INSCR AS CLI_IE, 
	A1_EMAIL AS CLI_EMAIL, 
	C5_EMISSAO AS EMISSAO, 
	C5_NUM AS PEDIDO, 
	C5_CONDPAG AS CONDICAO_PAG, 
	CASE 
		WHEN C5_FECENT <> '' THEN C5_FECENT ELSE C5_SUGENT -- Este dado não foi encontrado verificar se existe no cliente
	END AS DATA_ENTRAGA, 
	CASE 
		WHEN C5_FECENT <> '' THEN 'C5_FECENT' ELSE 'C5_SUGENT' -- Este dado não foi encontrado verificar se existe no cliente
	END AS DATA_ENTRAGA_ORI, 
	A3_COD + ' - ' + A3_NOME AS REPRESENTANTE 
FROM 
	SC5010 AS SC5 
	INNER JOIN 
		SA1010 AS SA1 
		ON C5_CLIENTE = A1_COD 
		AND C5_LOJACLI = A1_LOJA 
		AND A1_FILIAL = '10' 
		AND SA1.D_E_L_E_T_ = '' 
	INNER JOIN 
		SA3010 AS SA3 
		ON C5_VEND1 = A3_COD 
		AND A3_FILIAL = '10' 
		AND SA3.D_E_L_E_T_ = '' 
WHERE 
	C5_NUM = '094349' 
	AND C5_FILIAL = '10' 
	AND SC5.D_E_L_E_T_ = '' 

-- Quadro de itens do pedido com impostos

SELECT 
	C6_ITEM AS ITEM, 
	C6_DESCONT AS DESCON, 
	C6_QTDVEN AS QUANT, 
	B1_QE AS EMB, 
	C6_UM AS UNIDADE, 
	B1_COD AS CODIGO_FAMA, 
	B1_DESC AS DESCRICAO, 
	C6_PRCVEN AS VALOR_UNI, 
	C6_VALOR AS VALOR_TOTAL, 
	0.00 AS IPI_PORCENT, 
	0.00 AS IPI_VAL 
FROM 
	SC5010 AS SC5 
	INNER JOIN 
		SC6010 AS SC6 
		ON C5_NUM = C6_NUM 
		AND C6_FILIAL = '10' 
		AND SC6.D_E_L_E_T_ = '' 
	INNER JOIN 
		SB1010 AS SB1 
		ON C6_PRODUTO = B1_COD 
		AND B1_FILIAL = '10' 
		AND SB1.D_E_L_E_T_ = '' 
WHERE 
	C5_FILIAL = '10' 
	AND C5_NUM = '097173' 
	AND SC5.D_E_L_E_T_ = '' 
ORDER BY 
	C6_ITEM 

-- Grupo de produto

SELECT 
	B1_GRUPO AS GRUPO_COD, 
	BM_DESC AS GRUPO_DESCRI, 
	-- 'C6_UDCIDP + C6_UDCVEND + C6_UDCGPPD + C6_UDCCLI' AS DESCONTOS, 
	SUM(C6_QTDVEN * B1_PESO) AS PESO, 
	SUM(C6_VALOR) AS VALOR_LIQUIDO 
FROM 
	SC5010 AS SC5 
	INNER JOIN 
		SC6010 AS SC6 
		ON C5_FILIAL = C6_FILIAL 
		AND C5_NUM = C6_NUM 
		AND SC6.D_E_L_E_T_ = '' 
	INNER JOIN 
		SB1010 AS SB1 
		ON C6_PRODUTO = B1_COD 
		AND B1_FILIAL = '10' 
		AND SB1.D_E_L_E_T_ = '' 
	INNER JOIN 
		SBM010 AS SBM 
		ON B1_GRUPO = BM_GRUPO 
		AND BM_FILIAL = '10' 
		AND SBM.D_E_L_E_T_ = '' 
WHERE 
	C5_FILIAL = '10' 
	AND C5_NUM = '097173' 
	AND SC5.D_E_L_E_T_ = '' 
GROUP BY 
	B1_GRUPO, BM_DESC 